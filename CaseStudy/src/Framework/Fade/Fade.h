#ifndef __H_FADE_H__
#define __H_FADE_H__
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// 列挙体定義
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//------------------------------------------------
// フェードモード
//------------------------------------------------
typedef enum {
	FADE_NONE,
	FADE_IN,
	FADE_OUT,
	kFadeMax
} Fades;


//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
// プロトタイプ宣言
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//------------------------------------------------
// フェードの初期化
//------------------------------------------------
HRESULT InitFade( void );

//------------------------------------------------
// フェードの終了処理
//------------------------------------------------
void UninitFade( void );

//------------------------------------------------
// フェードの更新処理
// フェードアウト完了時にtrueを返す
// それ以外はfalse
//------------------------------------------------
bool UpdateFade( void );

//------------------------------------------------
// フェードの描画処理
//------------------------------------------------
void DrawFade( void );

//------------------------------------------------
// フェードの設定
//------------------------------------------------
void SetFade( Fades fade );

#endif // ifndef __H_FADE_H__
