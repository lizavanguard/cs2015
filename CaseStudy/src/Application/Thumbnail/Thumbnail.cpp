//------------------------------------------------
// ctor
//------------------------------------------------
Thumbnail::Thumbnail(const D3DXVECTOR3& position, const D3DXVECTOR2& size, const char* p_texture_filename)
    : Object2D(position, size, p_texture_filename)
    , is_active_(false) {
  Deactivate();
}

