//------------------------------------------------
// Activate
//------------------------------------------------
void Thumbnail::Activate(void) {
  is_active_ = true;
  color_ = 0xffffffff;
}

//------------------------------------------------
// Deactivate
//------------------------------------------------
void Thumbnail::Deactivate(void) {
  is_active_ = false;
  color_ = kDeactivateColor;
}
